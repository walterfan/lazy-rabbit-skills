#!/usr/bin/env bash

script_id() {
  local script_path="$1"
  printf 'scripts/%s\n' "$(basename "$script_path")"
}

generated_output_path() {
  local doc_dir="$1"
  local filename="$2"
  local output_dir="$doc_dir/_generated"
  mkdir -p "$output_dir"
  printf '%s/%s\n' "$output_dir" "$filename"
}

write_markdown_artifact() {
  local target="$1"
  local script_name="$2"
  local title="$3"
  local provenance_block="$4"
  local body_block="$5"
  local needs_input_block="$6"

  mkdir -p "$(dirname "$target")"
  {
    printf '<!-- Auto-generated by %s -->\n' "$script_name"
    printf '<!-- Deterministic extraction only. Mechanical extraction does not replace understanding. -->\n\n'
    printf '# %s\n\n' "$title"
    printf '> Mechanical extraction only. Treat this file as bounded input for human review or a targeted LLM update, not as final documentation.\n\n'
    if [[ -n "$provenance_block" ]]; then
      printf '## Provenance\n\n%s\n\n' "$provenance_block"
    fi
    printf '%s\n' "$body_block"
    if [[ -n "$needs_input_block" ]]; then
      printf '\n## Human / LLM Follow-Up\n\n%s\n' "$needs_input_block"
    fi
  } > "$target"
}
