#!/usr/bin/env python3
"""
Shared helpers for deterministic PKB script artifacts.
"""

from __future__ import annotations

import json
import os
from datetime import datetime, timezone
from pathlib import Path
from typing import Iterable, Sequence


def script_id(script_path: str) -> str:
    return f"scripts/{os.path.basename(script_path)}"


def generated_output_path(doc_dir: str, filename: str) -> str:
    output_dir = Path(doc_dir).resolve() / "_generated"
    output_dir.mkdir(parents=True, exist_ok=True)
    return str(output_dir / filename)


def write_text_file(path: str, content: str) -> None:
    target = Path(path)
    target.parent.mkdir(parents=True, exist_ok=True)
    target.write_text(content, encoding="utf-8")


def _sorted_unique(items: Iterable[str]) -> list[str]:
    return sorted({item for item in items if item})


def render_markdown_artifact(
    *,
    script_name: str,
    title: str,
    body: str,
    provenance: Sequence[str] | None = None,
    needs_input: Sequence[str] | None = None,
) -> str:
    lines = [
        f"<!-- Auto-generated by {script_name} -->",
        "<!-- Deterministic extraction only. Mechanical extraction does not replace understanding. -->",
        "",
        f"# {title}",
        "",
        "> Mechanical extraction only. Treat this file as bounded input for human review or a targeted LLM update, not as final documentation.",
        "",
    ]

    provenance_items = _sorted_unique(provenance or [])
    if provenance_items:
        lines.extend(["## Provenance", ""])
        lines.extend([f"- `{item}`" for item in provenance_items])
        lines.append("")

    lines.append(body.rstrip())
    lines.append("")

    review_items = _sorted_unique(needs_input or [])
    if review_items:
        lines.extend(["## Human / LLM Follow-Up", ""])
        lines.extend([f"- [NEEDS INPUT: {item}]" for item in review_items])
        lines.append("")

    return "\n".join(lines).rstrip() + "\n"


def render_json_artifact(
    *,
    script_name: str,
    payload: object,
    provenance: Sequence[str] | None = None,
    needs_input: Sequence[str] | None = None,
) -> str:
    document = {
        "_meta": {
            "auto_generated_by": script_name,
            "generated_at": datetime.now(timezone.utc).isoformat(),
            "deterministic_extraction_only": True,
            "provenance": _sorted_unique(provenance or []),
            "needs_input": [f"[NEEDS INPUT: {item}]" for item in _sorted_unique(needs_input or [])],
        },
        "data": payload,
    }
    return json.dumps(document, indent=2, ensure_ascii=False) + "\n"
